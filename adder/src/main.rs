use adder::adder;

fn main() {
    println!("adder(0, 0) = {}", adder(0, 0));
    println!("adder(1, 0) = {}", adder(1, 0));
    println!("adder(0, 1) = {}", adder(0, 1));
    println!("adder(1, 1) = {}", adder(1, 1));
    println!("adder(1, 2) = {}", adder(1, 2));
    println!("adder(2, 2) = {}", adder(2, 2));
}
